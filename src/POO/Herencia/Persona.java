package POO.Herencia;


public class Persona {
    String nombre;
    String apellido;

    int edad;
    String fechaNacimiento;

    public Persona(String nombre, String apellido, int edad, String fechaNacimiento) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
        this.fechaNacimiento = fechaNacimiento;
    }
}
